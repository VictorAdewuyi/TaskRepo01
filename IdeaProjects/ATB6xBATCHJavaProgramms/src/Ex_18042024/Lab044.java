package Ex_18042024;

public class Lab044 {
    public static void main(String[] args) {

        // Increment (++)  / Decrement (--) Operators
        // There are 2 types of these- Pre increment  and Post Increment

        // Pre Increment- Value is Inremented first and then the result is computed
        // Example of Pre Increment
        int a = 10;
        int b = ++a; //  a = a+1

        // b =11
        // a =10
        System.out.println(b);
        System.out.println(a);
        // ++a --------a = a+1 ( value increased first  and then assign the value)




    }
}
