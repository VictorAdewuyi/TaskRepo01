package Ex_18042024;

public class Lab047 {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(++a);
        //result ---->11
        //a-----> 11
        System.out.println(a);

        //How to remember ( Pre & Post)
       // int a = 10;
        System.out.println(++a);
        // Pre Increment
       // When you have ++ in front of variable like a ----> Use Pre-Increment
        // In this case, do increment first , then print


        int a1= 10;
        System.out.println(a1++);
        System.out.println(a1);
        // Post Increment
        // When you have ++ after variable like a ----> Use Post-Increment
        // In this case, print first then do increment first



    }


}
