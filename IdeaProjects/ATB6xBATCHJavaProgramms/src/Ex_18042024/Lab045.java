public class Lab045 {
    public static void main(String[] args) {

        // Post Increment- Value is first used for computing, then result is then Inremented or Deremented.
        // Example of Post Increment
        int a = 10;
        int b = a++; //  But you have to remember the value of a and b
        // In this case, b = 10 while a = 11
        System.out.println(b);
        // ++a --------a = a+1 ( value increased first  and then assign the value)

    }
}
